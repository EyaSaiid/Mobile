public class Back_AjoutCategoriePlat  extends BaseForm{
    Form current;
    public Back_AjoutCategoriePlat (Form current) {
       
        setTitle("Add a new category");
        setLayout(BoxLayout.y());
        
        TextField tfName = new TextField("","CategoryName");
        //TextField tfStatus= new TextField("", "Status: 0 - 1");
        Button btnValider = new Button("Add categorie");
        
        btnValider.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent evt) {
                if ((tfName.getText().length()==0))
                    Dialog.show("Alert", "Please fill all the fields", new Command("OK"));
                else
                {
                    try {
                        Categorie t = new Categorie(tfName.getText());
                        if( CatgeorieService.getInstance().addCategorie(t))
                            Dialog.show("Success","Connection accepted",new Command("OK"));
                        else
                            Dialog.show("ERROR", "Server error", new Command("OK"));
                    } catch (NumberFormatException e) {
                        Dialog.show("ERROR", "Status must be a number", new Command("OK"));
                    }
                    
                }
                
                
            }
        });
        
        addAll(tfName,btnValider);
        
        
                
    }
}
